//Arithmetic Operators-> +,-,*,/,%
#include <iostream>
using namespace std;
int main(){
    int a = 10;
    int b = 3;
    cout << "Addition: " << a + b << endl; // 13
    cout << "Subtraction: " << a - b << endl; // 7
    cout << "Multiplication: " << a * b << endl; // 30
    cout << "Division: " << a / b << endl; // 3
    cout << "Modulus: " << a % b << endl; // 1
    return 0;
}