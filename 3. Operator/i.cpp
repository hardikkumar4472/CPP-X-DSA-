//Logical Operators-> Convert to binary and result in decimal
// Logical Operators
#include <iostream>
using namespace std;
int main(){
    bool a = 4;
    bool b = 5;
    cout << "Logical AND: " << (a && b) << endl; 
    cout << "Logical OR: " << (a || b) << endl; 
    cout << "Logical NOT: " << (!a) << endl; 
    return 0;
}